import os
import yaml
import pandas as pd

from argparse import ArgumentParser

def parse_arguments():
    parser = ArgumentParser()
    parser.add_argument('experiment_base_path', help='Path to the experiment directory')
    
    parser.add_argument('network_dataset_id', help='ID of the network used in the experiment (e.g. alexnet_cifar10).')

    parser.add_argument('configuration_id', help='ID of the hardware configuration from which the error models were derived. ' \
                        'Using the shorthand version is recommended (e.g. 8x8_int8).')

    parser.add_argument('vulnerability_excel_path', help='Path to the Excel file that will be produced or modified with the new data (layer vulnerability values only).')

    parser.add_argument('metrics_excel_path', help='Path to the Excel file that wiil be created or modified with the new data (detailed per-layer metrics).')

    parser.add_argument('-al', '--applev_path', nargs='?', default=None, const=None, 
                        help='Relative path to the applev report YAML file generated by the experiment run.')
    
    parser.add_argument('-sdc', '--sdc_freq_path', nargs='?', default=None, const=None, 
                        help='Relative path to the Excel file containing the SDC frequencies for all injected layers, generated by the experiment run.')
    
    parser.add_argument('-nc', '--netcontent_path', nargs='?', default=None, const=None, 
                        help='Relative path to the netcontent.yaml file for the simulated network.')

    parser.add_argument('-randclass', '--random_spatial_classes', action='store_true', 
                        help='If set, signals that the spatial classes for the experiment were randomly sampled and their frequencies should not be weighed.')

    return parser.parse_args()


def find_default_file(candidate_filenames: list[str], beginning_token: str):
    possible_files = list(filter(lambda x: x.startswith(beginning_token), candidate_filenames))
    if len(possible_files) != 1:
        raise FileNotFoundError(f'Found either 0 or more than 1 possible "{beginning_token}" files in base directory. Provide a specific path.')

    return possible_files[0]


def snakecase_to_pascalcase(snake: str):
    tokens = snake.split('_')
    return ''.join(token.capitalize() for token in tokens)


if __name__ == '__main__':
    args = parse_arguments()

    base_path          = os.path.realpath(args.experiment_base_path)
    network_dataset_id = args.network_dataset_id
    configuration_id   = args.configuration_id
    
    vulnerability_excel_path = os.path.realpath(args.vulnerability_excel_path)
    metrics_excel_path       = os.path.realpath(args.metrics_excel_path)

    applev_path     = args.applev_path
    sdc_freq_path   = args.sdc_freq_path
    netcontent_path = args.netcontent_path

    random_spatial_classes = args.random_spatial_classes

    # CHECK PATHS AND LOAD FILES

    if not os.path.isdir(base_path):
        raise ValueError(f'Base experiment directory at {base_path} does not exist.')

    # if paths are missing, look for default files
    exp_dir_files = list(filter(lambda x: not os.path.isdir(x), os.listdir(base_path)))

    if applev_path is None:
        applev_path = os.path.join(base_path, find_default_file(exp_dir_files, 'applev'))
    
    if sdc_freq_path is None:
        sdc_freq_path = os.path.join(base_path, find_default_file(exp_dir_files, 'SDC'))

    if netcontent_path is None:
        netcontent_path = os.path.join(base_path, find_default_file(exp_dir_files, 'netcontent'))

    # extra existence checks
    if not os.path.exists(applev_path):
        raise FileNotFoundError(f'applev report file at {applev_path} does not exist.')
    if not os.path.exists(sdc_freq_path):
        raise FileNotFoundError(f'SDC frequencies Excel file at {sdc_freq_path} does not exist.')
    if not os.path.exists(netcontent_path):
        raise FileNotFoundError(f'Netcontent YAML file at {netcontent_path} does not exist.')

    if not vulnerability_excel_path.endswith('.xlsx'):
        vulnerability_excel_path = vulnerability_excel_path + '.xlsx'
    if not metrics_excel_path.endswith('.xlsx'):
        metrics_excel_path = metrics_excel_path + '.xlsx'

    # load files
    with open(applev_path) as f:
        applev_dict: dict = yaml.load(f, yaml.SafeLoader)
    with open(netcontent_path) as f:
        netcontent_dict = yaml.load(f, yaml.SafeLoader)
    netcontent_modules: dict = netcontent_dict['modules']
    
    sdc_freqs = pd.read_excel(sdc_freq_path, sheet_name=0, index_col=0, header=0)

    # COMPUTE METRICS

    vulnerability_layer_values = {}
    metrics_df_rows = []

    # check that all 3 layer lists are the same and create the rows
    for layer_name, layer_dict in applev_dict.items():
        if layer_name not in netcontent_modules.keys():
            raise ValueError(f'Layer {layer_name} is in the applev report, but not in the netcontent file.')
        if layer_name not in sdc_freqs.index:
            raise ValueError(f'Layer {layer_name} is in the applev report, but not in the SDC frequencies file.')

        # get layer time exposure
        time_exposure = float(netcontent_modules[layer_name]['seu-prob'])
        # get layer SDC frequency
        layer_sdc_freq = float(sdc_freqs.at[layer_name, 'SDC'])

        # get layer experiment outcome frequencies (application-level)
        if random_spatial_classes:
            # no need to rescale spatial class frequencies: use the total ones already in applev
            app_masked_prob   = layer_dict['prob_masked']
            app_safe_prob     = layer_dict['prob_sdc_safe']
            app_critical_prob = layer_dict['prob_sdc_critical']
        else:
            # the spatial classes were uniformly sampled: rescale each one's frequencies by the class SDC frequencies
            app_masked_prob = app_safe_prob = app_critical_prob = 0.0

            for spatial_class_name, spatial_class_dict in layer_dict.items():
                # skip non-spatial classes
                if spatial_class_name.startswith('prob'):
                    continue
                
                class_name_pascal = snakecase_to_pascalcase(spatial_class_name)
                if class_name_pascal in sdc_freqs.columns:
                    spatial_class_freq = float(sdc_freqs.at[layer_name, snakecase_to_pascalcase(spatial_class_name)])
                    app_masked_prob   += spatial_class_freq * spatial_class_dict['masked']
                    app_safe_prob     += spatial_class_freq * spatial_class_dict['sdc_safe']
                    app_critical_prob += spatial_class_freq * spatial_class_dict['sdc_critical']


        # rescale total layer frequencies by SDC frequency (probability for the SEU to affect the layer output)
        arch_masked_prob   = layer_sdc_freq * app_masked_prob
        arch_safe_prob     = layer_sdc_freq * app_safe_prob
        arch_critical_prob = layer_sdc_freq * app_critical_prob

        # rescale frequencies by time exposure (probability for a SEU to strike the layer)
        time_masked_prob   = time_exposure * arch_masked_prob 
        time_safe_prob     = time_exposure * arch_safe_prob 
        time_critical_prob = time_exposure * arch_critical_prob


        # build metrics row
        metrics_df_rows.append(pd.Series({
            'Layer': layer_name,
            'Time exposure': time_exposure,
            'SDC freq': layer_sdc_freq,
            'Masked err freq': app_masked_prob,
            'Safe err freq': app_safe_prob,
            'Critical err freq': app_critical_prob,
            'Vulnerability(crit * sdc)': arch_critical_prob,
            'Vulnerability(crit * sdc * time)': time_critical_prob,
        }))

        # record vulnerability value for the layer
        vulnerability_layer_values[layer_name] = time_critical_prob

    metrics_df = pd.DataFrame(metrics_df_rows).set_index('Layer')

    # SAVE TO FILES

    metrics_sheet_name = f'{network_dataset_id}_{configuration_id}'

    if not os.path.exists(metrics_excel_path):
        metrics_df.to_excel(metrics_excel_path, sheet_name=metrics_sheet_name, index_label='Layer')
    else:
        with pd.ExcelWriter(metrics_excel_path, mode='a', engine='openpyxl', if_sheet_exists='error') as writer:
            metrics_df.to_excel(writer, sheet_name=metrics_sheet_name, index_label='Layer')
    

    if not os.path.exists(vulnerability_excel_path):
        # file does not exist yet or file exists, but sheet does not: create new vulnerability dataframe
        vuln_df = pd.DataFrame(index=vulnerability_layer_values.keys())
        vuln_df.index.name = 'Layer'
        vuln_df.loc[:, configuration_id] = pd.Series(vulnerability_layer_values)
        vuln_df.to_excel(vulnerability_excel_path, sheet_name=network_dataset_id, index_label='Layer')
    else:
        if network_dataset_id in pd.ExcelFile(vulnerability_excel_path).sheet_names:
            # file and sheet both exist: append to file
            vuln_df = pd.read_excel(vulnerability_excel_path, sheet_name=network_dataset_id, header=0, index_col=0)
        else:
            # sheet does not exist: create new dataframe
            vuln_df = pd.DataFrame(index=vulnerability_layer_values.keys())
            vuln_df.index.name = 'Layer'

        vuln_df.loc[:, configuration_id] = pd.Series(vulnerability_layer_values)

        with pd.ExcelWriter(vulnerability_excel_path, mode='a', engine='openpyxl', if_sheet_exists='replace') as writer:
            vuln_df.to_excel(writer, sheet_name=network_dataset_id, index_label='Layer')