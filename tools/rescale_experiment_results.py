import os
import yaml
import pandas as pd

from argparse import ArgumentParser

def parse_arguments():
    parser = ArgumentParser()
    parser.add_argument('overall_report_yaml_path', help='Path to the overall report YAML file generated by the experiment run.')
    parser.add_argument('sdc_freq_yaml_path', help='Path to the YAML file containing the SDC frequency for each injected layer.')
    parser.add_argument('netcontent_yaml_path', help='Path to the netcontent.yaml file for the simulated network.')
    parser.add_argument('final_excel_path', help='Path to the Excel file that will be produced. The final result will be saved ' \
                        'in a sheet named after the experiment network. If the file already exists, the new sheet will be added to it.')

    return vars(parser.parse_args())


if __name__ == '__main__':
    args = parse_arguments()

    overall_report_path = os.path.realpath(args['overall_report_yaml_path'])
    sdc_freq_path = os.path.realpath(args['sdc_freq_yaml_path'])
    netcontent_path = os.path.realpath(args['netcontent_yaml_path'])

    if not os.path.exists(overall_report_path):
        raise FileNotFoundError(f'Overall experiment report file at {overall_report_path} does not exist.')
    if not os.path.exists(sdc_freq_path):
        raise FileNotFoundError(f'SDC frequencies YAML file at {sdc_freq_path} does not exist.')
    if not os.path.exists(netcontent_path):
        raise FileNotFoundError(f'Netcontent YAML file at {netcontent_path} does not exist.')

    final_output_path = os.path.realpath(args['final_excel_path'])
    if not final_output_path.endswith('.xlsx'):
        final_output_path = final_output_path + '.xlsx'

    # load files
    with open(overall_report_path) as f:
        overall_report = yaml.load(f, yaml.SafeLoader)
    with open(sdc_freq_path) as f:
        sdc_freq_dict: dict = yaml.load(f, yaml.SafeLoader)
    with open(netcontent_path) as f:
        netcontent_dict = yaml.load(f, yaml.SafeLoader)

    experiment_layer_statistics: dict = overall_report['Error simulation data']['Per-layer statistics']
    netcontent_modules: dict = netcontent_dict['modules']

    output_df_rows = []

    # check that all 3 layer lists are the same and create the rows
    for report_layer_name, report_layer_dict in experiment_layer_statistics.items():
        if report_layer_name not in netcontent_modules.keys():
            raise ValueError(f'Layer {report_layer_name} is in the overall report, but not in the netcontent file.')
        if report_layer_name not in sdc_freq_dict.keys():
            raise ValueError(f'Layer {report_layer_name} is in the overall report, but not in the SDC frequencies file.')

        # count layer outcomes
        masked = sdc_safe = sdc_critical = 0

        for layer_statistics in report_layer_dict.values():
            masked += layer_statistics['Masked']
            sdc_safe += layer_statistics['SDC safe']
            sdc_critical += layer_statistics['SDC critical']

        # compute final SDC critical frequency
        misclassification_freq = sdc_critical / (masked + sdc_safe + sdc_critical)

        # get SDC frequency
        sdc_freq: float = sdc_freq_dict[report_layer_name]

        # get time exposure
        time_exposure: float = netcontent_modules[report_layer_name]['seu-prob']

        # build layer row
        output_df_rows.append(pd.Series({
            'Layer': report_layer_name,
            'SDC frequency': sdc_freq,
            'Misclassification frequency': misclassification_freq,
            'Time exposure': time_exposure,
            'Vulnerability(no time)': sdc_freq * misclassification_freq,
            'Vulnerability': sdc_freq * misclassification_freq * time_exposure,
        }))

    output_df = pd.DataFrame(output_df_rows).set_index('Layer')

    network_name = f'{overall_report['Model name']}_{overall_report['Dataset data']['Dataset name']}'

    if not os.path.exists(final_output_path):
        output_df.to_excel(final_output_path, sheet_name=network_name, index_label='Layer')
    else:
        with pd.ExcelWriter(final_output_path, mode='a', engine='openpyxl') as writer:
            output_df.to_excel(writer, sheet_name=network_name, index_label='Layer')